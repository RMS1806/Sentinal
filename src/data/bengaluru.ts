/* ─── Slice-aware heatmap: points weighted by PRIORITY_QUEUE forecast ─── */
export function getHeatmapGeoJSONForSlice(key: string) {
  const entries = PRIORITY_QUEUE[key] ?? [];
  const maxFc = Math.max(...entries.map((e) => e.forecast), 1);
  const features: object[] = [];
  entries.forEach((entry) => {
    const zone = ZONES.find((z) => z.id === entry.zoneId);
    if (!zone) return;
    // number of scatter points proportional to forecast (scaled so max zone ≈ 80 pts)
    const nPts = Math.max(1, Math.ceil((entry.forecast / maxFc) * 80));
    const w = entry.impactIndex / 100;
    for (let i = 0; i < nPts; i++) {
      const seed = i * 13 + zone.id.charCodeAt(1);
      const lat = zone.lat + (Math.sin(seed * 0.7) * 0.007);
      const lon = zone.lon + (Math.cos(seed * 1.1) * 0.009);
      features.push({
        type: 'Feature',
        properties: { weight: w },
        geometry: { type: 'Point', coordinates: [parseFloat(lon.toFixed(5)), parseFloat(lat.toFixed(5))] },
      });
    }
  });
  return { type: 'FeatureCollection', features };
}

